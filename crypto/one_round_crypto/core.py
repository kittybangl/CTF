INV_S = [0] * 256
for i, v in enumerate(S):
    INV_S[v] = i


def xor(a, b):
    return bytes([x ^ y for x, y in zip(a, b)])


def rotr(chunk, r, size):
    val = int.from_bytes(chunk, 'big')
    val = (val >> r) | ((val & (1 << r) - 1) << (8 * size - r))
    return int.to_bytes(val, size, 'big')


def update_key(k):
    return sha256(k).digest()


def pad_pkcs7(msg, block_size):
    padlen = block_size - (len(msg) % block_size)
    return msg + bytes([padlen]) * padlen


def mix(msg, current_key):
    random.seed(current_key)
    for _ in range(MIXING_ROUNDS):
        chunks = [msg[i:i + DIM // 4] for i in range(0, DIM, DIM // 4)]
        for i in range(4):
            chunks[i] = xor(chunks[i], xor(rotr(chunks[i], random.randint(1, 2 * DIM), DIM // 4), rotr(chunks[i], random.randint(1, 2 * DIM), DIM // 4)))
        msg = b''.join(chunks)
        msg = xor(msg, xor(rotr(msg, DIM // 2 - 1, DIM), rotr(msg, DIM // 2 + 1, DIM)))
    return msg


def sub(msg, current_key):
    keyed_s = []
    for i in range(DIM):
        keyed_s.append([(x + current_key[i]) % 256 for x in S])
    return bytes([keyed_s[i][x] for i, x in enumerate(msg)])


def enc_block(block, key):
    current_key = update_key(key)
    encrypted = mix(block, current_key)
    current_key = update_key(current_key)
    encrypted = sub(encrypted, current_key)
    current_key = update_key(current_key)
    encrypted = mix(encrypted, current_key)
    return encrypted


def enc(msg_hex, key):
    msg = pad_pkcs7(bytes.fromhex(msg_hex), DIM)
    blocks = [msg[i:i + DIM] for i in range(0, len(msg), DIM)]
    return b''.join(enc_block(b, key) for b in blocks).hex()


def lin_cols(current_key):
    out = []
    for i in range(128):
        v = (1 << (127 - i)).to_bytes(16, 'big')
        out.append(int.from_bytes(mix(v, current_key), 'big'))
    return out


def gf2_inv_matrix(cols):
    aug = []
    for r in range(128):
        row = 0
        for c in range(128):
            if (cols[c] >> (127 - r)) & 1:
                row |= 1 << (127 - c)
        aug.append((row << 128) | (1 << (127 - r)))

    for col in range(128):
        pivot = None
        for r in range(col, 128):
            if (aug[r] >> (255 - col)) & 1:
                pivot = r
                break
        if pivot is None:
            raise RuntimeError('matrix not invertible')
        aug[col], aug[pivot] = aug[pivot], aug[col]
        for r in range(128):
            if r != col and ((aug[r] >> (255 - col)) & 1):
                aug[r] ^= aug[col]

    inv = [0] * 128
    for r in range(128):
        right = aug[r] & ((1 << 128) - 1)
        for c in range(128):
            if (right >> (127 - c)) & 1:
                inv[c] |= 1 << (127 - r)
    return inv


def apply_lin(cols, xbytes):
    x = int.from_bytes(xbytes, 'big')
    y = 0
    for i, c in enumerate(cols):
        if (x >> (127 - i)) & 1:
            y ^= c
    return y.to_bytes(16, 'big')


def dec_block(c, key):
    k1 = update_key(key)
    k2 = update_key(k1)
    k3 = update_key(k2)

    inv_l3 = gf2_inv_matrix(lin_cols(k3))
    inv_l1 = gf2_inv_matrix(lin_cols(k1))

    y = apply_lin(inv_l3, c)
    mid = bytes(INV_S[(y[i] - k2[i]) % 256] for i in range(16))
    return apply_lin(inv_l1, mid)
